int jpeg_decode(const char *file)
{
    DWORD _data_position;
    DWORD __data_position;
    DWORD i;
    WORD length;
    BYTE index;
    BYTE found_app0 = 0,
         found_dht  = 0,
         found_dqt  = 0,
         found_sofn = 0,
         found_sos  = 0;

    //read file
    if (! jpeg_decoder_read_file(file)) return 0;

    //find SOI marker
    if (
         (! jpeg_decoder_seek_marker())
         ||
         (data_byte != SOI)
       )
        ERROR("invalid file format");

    while (! END_OF_DATA)
    {
        //find next marker
        if (! jpeg_decoder_seek_marker()) ERROR("invalid file format");
        if (data_byte == EOI)
        {
            if (found_sos)
            {
                if (flag_mode == SOF2) jpeg_pdecoder_process_data();
                return 1;
            };

            return 0;
        };

        //get header length
        GET_WORD();
        length         = data_word - 2;
        _data_position = data_position;

        switch (data_byte)
        {
        case APP0:
//.....................................................................................................
            found_app0 = 1;

            //validate file type
            if (
                 (data[data_position + 0] != 0x4A) //J
                 ||
                 (data[data_position + 1] != 0x46) //F
                 ||
                 (data[data_position + 2] != 0x49) //I
                 ||
                 (data[data_position + 3] != 0x46) //F
                 ||
                 (data[data_position + 4] != 0x00) //zero termination
                 ||
                 (data[data_position + 5] != 0x01) //version 1.x
               )
                ERROR("invalid file format");

            break;

        case DHT:
//.....................................................................................................
            found_dht = 1;

            for (i = 0; i < length; i += data_position - __data_position)
            {
                __data_position = data_position;
                GET_BYTE();
                index           = BYTE_LOW(data_byte);
                if (index > 3) ERROR("invalid huffman table index");
                if (data_byte & 0x10)
                {
                    if (! htable_ac[index]) htable_ac[index] = (HTABLE *)malloc(sizeof(HTABLE));
                    if (! htable_ac[index]) ERROR("out of memory");
                    jpeg_decoder_read_htable(htable_ac[index]);
                }
                else
                {
                    if (! htable_dc[index]) htable_dc[index] = (HTABLE *)malloc(sizeof(HTABLE));
                    if (! htable_dc[index]) ERROR("out of memory");
                    jpeg_decoder_read_htable(htable_dc[index]);
                };
            };

            break;

        case DQT:
//.....................................................................................................
            found_dqt = 1;

            for (i = 0; i < length; i += data_position - __data_position)
            {
                __data_position = data_position;
                GET_BYTE();
                if (data_byte & 0xF0) ERROR("precision not supported");
                if (data_byte > 2)    ERROR("invalid quantization table index");
                jpeg_decoder_read_qtable(qtable[data_byte]);
            };

            break;

        case SOF0:  //baseline
        case SOF1:  //extended sequential
        case SOF2:  //progressive
        case SOF3:  //lossless
        case SOF5:  //differential sequential
        case SOF6:  //differential progressive
        case SOF7:  //differential lossless
        case SOF9:  //extended sequential, arithmetic coding
        case SOF10: //progressive, arithmetic coding
        case SOF11: //lossless, arithmetic coding
        case SOF13: //differential sequential, arithmetic coding
        case SOF14: //differential progressive, arithmetic coding
        case SOF15: //differential lossless, arithmetic coding
//.....................................................................................................
            found_sofn     = 1;
            flag_mode      = data_byte;

            //mode
            if (flag_mode != SOF0 && flag_mode != SOF2) ERROR("mode not supported");

            //precision
            GET_BYTE();
            if (data_byte != 8)                         ERROR("precision not supported");

            //image height
            GET_WORD();
            img_height     = data_word;

            //image width
            GET_WORD();
            img_width      = data_word;

            //component count
            GET_BYTE();
            if (data_byte != 1 && data_byte != 3)       ERROR("component model not supported");
            cmp_count      = data_byte;

            //component data
            for (i = 0; i < cmp_count; i++)
            {
                __data_position     = data_position;
                GET_BYTE();
                if (! data_byte || data_byte > 3)           ERROR("invalid component index");
                index               = data_byte - 1;        //component id
                cmp[index].index    = index;
                GET_BYTE();
                cmp[index].factor_x = BYTE_HIGH(data_byte); //horizontal sampling factor
                cmp[index].factor_y = BYTE_LOW(data_byte);  //vertical sampling factor
                GET_BYTE();
                cmp[index].qtable   = qtable[data_byte];    //quantization table id

                //maximum scaling factor
                if (cmp[index].factor_x > cmp_max_factor_x) cmp_max_factor_x = cmp[index].factor_x;
                if (cmp[index].factor_y > cmp_max_factor_y) cmp_max_factor_y = cmp[index].factor_y;
            };

            //component scaling factor
            for (i = 0; i < cmp_count; i++)
            {
                cmp[i].scale_x      = cmp_max_factor_x / cmp[i].factor_x;
                cmp[i].scale_y      = cmp_max_factor_y / cmp[i].factor_y;
                cmp[i].width        = cmp[i].factor_x * 8;
                cmp[i].height       = cmp[i].factor_y * 8;
            };

            //mcu size
            mcu_width      = cmp_max_factor_x * 8;
            mcu_height     = cmp_max_factor_y * 8;

            //buffer
            buffer.width   = img_width  % mcu_width;
            if (buffer.width)                           buffer.width  = mcu_width  - buffer.width;
            buffer.width  += img_width;

            buffer.height  = img_height % mcu_height;
            if (buffer.height)                          buffer.height = mcu_height - buffer.height;
            buffer.height += img_height;

            buffer.size    = buffer.width * buffer.height * sizeof(SWORD);

            for (i = 0; i < cmp_count; i++)
            {
                buffer.cmp[i]       = (SWORD *)malloc(buffer.size);
                if (buffer.cmp[i] == NULL)                  ERROR("out of memory");
                memset(buffer.cmp[i], 0, buffer.size);
            };

            for (i = 0; i < 64; i++) buffer_position[i] = i / 8 * buffer.width + i % 8;

            break;

        case SOS:
//.....................................................................................................
            /* APP0 header check disabled due to
               some digital cameras known to omit it */
            //if (! found_app0) ERROR("missing APP0 header");
            if (! found_dht)  ERROR("missing DHT header");
            if (! found_dqt)  ERROR("missing DQT header");
            if (! found_sofn) ERROR("missing SOFn header");

            found_sos = 1;

            //component count
            GET_BYTE();
            scan.cmp_count  = data_byte;
            if (scan.cmp_count > cmp_count) ERROR("invalid number of components");

            //component data
            for (i = 0; i < scan.cmp_count; i++)
            {
                GET_BYTE();
                if (! data_byte || data_byte > 3) ERROR("invalid component index");
                index                = data_byte - 1;                   //component id
                cmp[index].dc        = 0;
                GET_BYTE();
                cmp[index].htable_dc = htable_dc[BYTE_HIGH(data_byte)]; //dc huffman table id
                cmp[index].htable_ac = htable_ac[BYTE_LOW(data_byte)];  //ac huffman table id

                scan.cmp[i]          = &cmp[index];
            };

            //spectral selection start
            GET_BYTE();
            scan.ss_start   = data_byte;

            //spectral selection end
            GET_BYTE();
            scan.ss_end     = data_byte;

            //successive approximation high/low bit
            GET_BYTE();
            scan.sa_high    = BYTE_HIGH(data_byte);
            scan.sa_low     = BYTE_LOW(data_byte);

            data_position   = _data_position + length;

            //reset
            flag_marker     = 0;
            bitstring.count = 0;
            bitstring.value = 0;

            switch (flag_mode)
            {
            case SOF0:
                jpeg_decoder_process_scan();
                return 1;

            case SOF2:
                jpeg_pdecoder_process_scan();
                continue;
            };

            return 0;
        };

        data_position = _data_position + length;
    };

    return 0;
};
//-----------------------------------------------------------------------------------------------------
int jpeg_decoder_read_file(const char *file)
{
    //open file
    file_handle = fopen(file, "rb");
    if (file_handle == NULL) ERROR("file not found");

    //determine filesize
    fseek(file_handle, 0, SEEK_END);
    data_length = ftell(file_handle);

    //allocate buffer memory
    data        = (BYTE *)malloc(data_length);
    if (data == NULL)
    {
        fclose(file_handle);
        ERROR("out of memory");
    };

    //store file content in memory
    fseek(file_handle, 0, SEEK_SET);
    fread(data, 1, data_length, file_handle);
    fclose(file_handle);

    return 1;
};
//-----------------------------------------------------------------------------------------------------
int jpeg_decoder_seek_marker(void)
{
    do
    {
        do {if (END_OF_DATA) return 0; GET_BYTE();} while (data_byte != 0xFF);
        do {if (END_OF_DATA) return 0; GET_BYTE();} while (data_byte == 0xFF);
    }
    while (data_byte == 0x00);

    return ! END_OF_DATA;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_read_htable(HTABLE *htable)
{
    WORD code = 0;
    BYTE count;
    BYTE i;

    //read code length
    for (count = 0; count < 16; count++)
    {
        GET_BYTE();
        htable->count[count] = data_byte;
    };

    //code bit range
    htable->min_count = 0;

    //read code value
    for (count = 0; count < 16; count++)
    {
        for (i = 0; i < htable->count[count]; i++)
        {
            GET_BYTE();
            htable->value[WORD_HIGH_LOW(count, i)] = data_byte;
        };

        //compute category range
        if (htable->count[count])
        {
            htable->minor[count]  = code;
            code                 += htable->count[count];
            htable->major[count]  = code - 1;

            //htable->max_count     = count + 1;
            if (! htable->min_count) htable->min_count = count + 1;
        }
        else
        {
            htable->minor[count]  = 0xFFFF;
            htable->major[count]  = 0x0000;
        };

        code <<= 1;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_read_qtable(BYTE *qtable)
{
    BYTE i;

    for (i = 0; i < 64; i++)
    {
        GET_BYTE();
        qtable[i] = data_byte;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_process_scan(void)
{
    CMP *_cmp;
    DWORD img_x,       img_y;
    WORD img_offset_x, img_offset_y;
    WORD mcu_x,        mcu_y;
    BYTE i;
    BYTE du_x,         du_y;
    BYTE x1,           y1;
    BYTE _x1,          _y1;
    BYTE               __y1;
    BYTE x2,           y2;

    //process scan data
    for (mcu_y = 0; mcu_y < img_height; mcu_y += mcu_height) //mcu vertical
    {
        for (mcu_x = 0; mcu_x < img_width; mcu_x += mcu_width) //mcu horizontal
        {
            for (i = 0; i < scan.cmp_count; i++) //component
            {
                _cmp = scan.cmp[i];

                for (du_y = 0; du_y < _cmp->height; du_y += 8) //data unit vertical
                {
                    img_offset_y = mcu_y + du_y;

                    for (du_x = 0; du_x < _cmp->width; du_x += 8) //data unit horizontal
                    {
                        img_offset_x = mcu_x + du_x;

                        //decoding
                        jpeg_decoder_get_data_unit(_cmp->index);
                        jpeg_decoder_dequantize(_cmp->index);
                        jpeg_decoder_idct();

                        //subsampling
                        for (y1 = 0, _y1 = 0, __y1 = 0; y1 < 8; y1++, __y1 += 8)
                        {
                            for (y2 = 0; y2 < _cmp->scale_y; y2++)
                            {
                                img_y = img_offset_y + _y1 + y2;

                                if (img_y < img_height)
                                {
                                    img_y *= buffer.width;

                                    for (x1 = 0, _x1 = 0; x1 < 8; x1++)
                                    {
                                        for (x2 = 0; x2 < _cmp->scale_x; x2++)
                                        {
                                            img_x = img_offset_x + _x1 + x2;

                                            if (img_x < img_width)
                                                buffer.cmp[_cmp->index][img_x + img_y] = data_unit[x1 + __y1];
                                        };

                                        _x1 += _cmp->scale_x;
                                    };
                                };
                            };

                            _y1 += _cmp->scale_y;
                        };
                    }; //data unit horizontal
                }; //data unit vertical
            }; //component
        }; //mcu horizontal
    }; //mcu vertical
};
//-----------------------------------------------------------------------------------------------------
WORD jpeg_decoder_peek_bit(BYTE count)
{
    BYTE _data_byte;

    flag_restart = 0;

    while (bitstring.count < count && ! END_OF_DATA)
    {
        GET_BYTE();
        _data_byte = data_byte;

        if (data_byte == 0xFF)
        {
            while (data_byte == 0xFF) GET_BYTE();

            if (data_byte)
            {
                bitstring.value = 0;
                bitstring.count = 0;

                if (data_byte >= 0xD0 && data_byte <= 0xD7) //RST marker
                {
                    flag_restart   = 1;
                }
                else
                {
                    flag_marker    = 1;
                    data_position -= 2;
                };

                return 0;
            };
        };

        bitstring.value <<= 8;
        bitstring.value  |= _data_byte;
        bitstring.count  += 8;
    };

    return bitstring.value >> (bitstring.count - count);
};
//-----------------------------------------------------------------------------------------------------
WORD jpeg_decoder_get_bit(BYTE count)
{
    WORD value = jpeg_decoder_peek_bit(count);
    jpeg_decoder_clear_bit(count);

    return value;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_clear_bit(BYTE count)
{
    if (count > bitstring.count) bitstring.count  = 0;
    else                         bitstring.count -= count;
    bitstring.value &= bitmask1[bitstring.count];
};
//-----------------------------------------------------------------------------------------------------
BYTE jpeg_decoder_get_hvalue(HTABLE *htable)
{
    WORD code;
    BYTE i;
    BYTE count;

    i = htable->min_count;

    while (i <= 16)
    {
        count = i - 1;
        code  = jpeg_decoder_peek_bit(i);

        if (flag_marker) return 0;
        if (flag_restart)
        {
            for (i = 0; i < cmp_count; i++) cmp[i].dc = 0;
            scan.eobrun  = 0;
            i            = htable->min_count;
            continue;
        };

        if (code >= htable->minor[count] && code <= htable->major[count])
        {
            jpeg_decoder_clear_bit(i);
            code        -= htable->minor[count];
            return htable->value[WORD_HIGH_LOW(count, code)];
        };

        i++;
    };

    ERROR("invalid huffman code");
    return 0;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_get_data_unit(BYTE index)
{
    SWORD value;
    BYTE zero;
    BYTE count;
    BYTE i;
    BYTE c;

    //DC coefficient
    if (count = jpeg_decoder_get_hvalue(cmp[index].htable_dc))
    {
        value         = jpeg_decoder_get_bit(count);
        data_unit[0]  = cmp[index].dc + CONVERT_SWORD(value, count);
        cmp[index].dc = data_unit[0];
    }
    else
    {
        data_unit[0]  = cmp[index].dc;
    };

    //AC coefficient
    i = 1;

    while (i < 64)
    {
        value = jpeg_decoder_get_hvalue(cmp[index].htable_ac);
        count = BYTE_LOW(value);
        zero  = BYTE_HIGH(value);

        if (count == 0)
        {
            if (zero == 0)       break;  //EOB
            else if (zero == 15) zero++; //16 zero marker
        };

        for (c = i + zero; i < c; i++) data_unit[i] = 0;

        if (count)
        {
            value          = jpeg_decoder_get_bit(count);
            data_unit[i++] = CONVERT_SWORD(value, count);
        };
    };

    for (; i < 64; i++) data_unit[i] = 0;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_dequantize(BYTE index)
{
        SWORD _data_unit[64];
        BYTE i;

        for (i = 0; i < 64; i++) _data_unit[i] = data_unit[i] * cmp[index].qtable[i];
        for (i = 0; i < 64; i++) data_unit[i]  = _data_unit[stable[i]];
};
//-----------------------------------------------------------------------------------------------------
void jpeg_decoder_idct(void)
{
    SDWORD a0, a1, a2, a3, a4, a5, a6, a7, a8;
    SDWORD b0, b1, b2, b3, b4, b5, b6, b7, b8;
    SWORD *p;
    BYTE i;

    //process row
    p = data_unit;

    for (i = 0; i < 8; i++)
    {
        if ((p[0] | p[1] | p[2] | p[3] | p[4] | p[5] | p[6] | p[7]) == 0)
        {
            p += 8;
            continue;
        };

        a0     = (p[0] << 11) + 128;
        a1     =  p[1];
        a2     =  p[2];
        a3     =  p[3];
        a4     =  p[4] << 11;
        a5     =  p[5];
        a6     =  p[6];
        a7     =  p[7];

        b4     = 1108 * (a6 + a2);

        b8     = a0 + a4;
        b0     = a0 - a4;

        b6     = b4 - 3784 * a6;
        b2     = b4 + 1568 * a2;

        b3     = b8 + b2;
        b8    -= b2;

        b2     = b0 + b6;
        b0    -= b6;

        a8     =  565 * (a1 + a7);
        b7     = a8 - 3406 * a7;
        b1     = a8 + 2276 * a1;

        a8     = 2408 * (a3 + a5);
        a3     = a8 - 4017 * a3;
        b5     = a8 -  799 * a5;

        b4     = b1 + b5;
        b1    -= b5;

        b5     = b7 + a3;
        b7    -= a3;

        b6     = (181 * (b1 + b7) + 128) >> 8;
        b1     = (181 * (b1 - b7) + 128) >> 8;

        p[0]   = (b3 + b4) >> 8;
        p[1]   = (b2 + b6) >> 8;
        p[2]   = (b0 + b1) >> 8;
        p[3]   = (b8 + b5) >> 8;
        p[4]   = (b8 - b5) >> 8;
        p[5]   = (b0 - b1) >> 8;
        p[6]   = (b2 - b6) >> 8;
        p[7]   = (b3 - b4) >> 8;

        p     += 8;
    };

    //process column
    p = data_unit;

    for (i = 0; i < 8; i++)
    {
        if ((p[0] | p[8] | p[16] | p[24] | p[32] | p[40] | p[48] | p[56]) == 0)
        {
            p += 1;
            continue;
        };

        a0     = (p[ 0] << 8) + 8192;
        a1     =  p[ 8];
        a2     =  p[16];
        a3     =  p[24];
        a4     =  p[32] << 8;
        a5     =  p[40];
        a6     =  p[48];
        a7     =  p[56];

        b4     = 1108 * (a6 + a2) + 4;

        b8     = a0 + a4;
        b0     = a0 - a4;

        b6     = (b4 - 3784 * a6) >> 3;
        b2     = (b4 + 1568 * a2) >> 3;

        b3     = b8 + b2;
        b8    -= b2;

        b2     = b0 + b6;
        b0    -= b6;

        a8     =  565 * (a1 + a7) + 4;
        b7     = (a8 - 3406 * a7) >> 3;
        b1     = (a8 + 2276 * a1) >> 3;

        a8     = 2408 * (a3 + a5) + 4;
        a3     = (a8 - 4017 * a3) >> 3;
        b5     = (a8 -  799 * a5) >> 3;

        b4     = b1 + b5;
        b1    -= b5;

        b5     = b7 + a3;
        b7    -= a3;

        b6     = (181 * (b1 + b7) + 128) >> 8;
        b1     = (181 * (b1 - b7) + 128) >> 8;

        p[ 0]  = (b3 + b4) >> 14;
        p[ 8]  = (b2 + b6) >> 14;
        p[16]  = (b0 + b1) >> 14;
        p[24]  = (b8 + b5) >> 14;
        p[32]  = (b8 - b5) >> 14;
        p[40]  = (b0 - b1) >> 14;
        p[48]  = (b2 - b6) >> 14;
        p[56]  = (b3 - b4) >> 14;

        p     += 1;
    };

    for (i = 0; i < 64; i++)
    {
                data_unit[i] += 128;

        if (data_unit[i] < 0)        data_unit[i] = 0;
        else if (data_unit[i] > 255) data_unit[i] = 255;
    };
};

