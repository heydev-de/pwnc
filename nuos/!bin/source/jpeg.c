void jpeg_pdecoder_process_scan(void)
{
    CMP *_cmp;
    DWORD position;
    SWORD *_buffer;
    WORD img_offset_x, img_offset_y;
    void (*get_data_unit)(BYTE, SWORD *);

    //decoding
    if (scan.ss_start) //AC
    {
        if (scan.sa_high) get_data_unit = jpeg_pdecoder_ac_approximation;
        else              get_data_unit = jpeg_pdecoder_ac_base;
    }
    else //DC
    {
        if (scan.sa_high) get_data_unit = jpeg_pdecoder_dc_approximation;
        else              get_data_unit = jpeg_pdecoder_dc_base;
    };

    //process scan data
    if (scan.cmp_count == 1 && scan.ss_start)
    {
        BYTE width  = scan.cmp[0]->scale_x * 8;
        BYTE height = scan.cmp[0]->scale_y * 8;

        _cmp        = scan.cmp[0];

        for (img_offset_y = 0; img_offset_y < img_height; img_offset_y += height)
        {
            for (img_offset_x = 0; img_offset_x < img_width; img_offset_x += width)
            {
                position = img_offset_x + img_offset_y * buffer.width;
                _buffer  = &buffer.cmp[_cmp->index][position];

                get_data_unit(_cmp->index, _buffer);

                if (flag_marker) return;
            };
        };
    }
    else
    {
        WORD mcu_x, mcu_y;
        BYTE du_x,  du_y;
        BYTE i;

        for (mcu_y = 0; mcu_y < img_height; mcu_y += mcu_height) //mcu vertical
        {
            for (mcu_x = 0; mcu_x < img_width; mcu_x += mcu_width) //mcu horizontal
            {
                for (i = 0; i < scan.cmp_count; i++) //component
                {
                    _cmp = scan.cmp[i];

                    for (du_y = 0; du_y < _cmp->height; du_y += 8) //data unit vertical
                    {
                        img_offset_y = mcu_y + du_y;

                        for (du_x = 0; du_x < _cmp->width; du_x += 8) //data unit horizontal
                        {
                            img_offset_x = mcu_x + du_x;
                            position     = img_offset_x + img_offset_y * buffer.width;
                            _buffer      = &buffer.cmp[_cmp->index][position];

                            get_data_unit(_cmp->index, _buffer);

                            if (flag_marker) return;
                        }; //data unit horizontal
                    }; //data unit vertical
                }; //component
            }; //mcu horizontal
        }; //mcu vertical
    };
};
