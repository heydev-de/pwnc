int jpeg_encode(const char *file)
{
    //mcu
    WORD mcu_x,            mcu_y;
    BYTE mcu_width,        mcu_height;

    //image
    DWORD img_x,           img_y;
    DWORD _img_x,          _img_y;
    WORD img_offset_x,     img_offset_y;

    //component
    BYTE cmp_max_factor_x, cmp_max_factor_y;
    BYTE cmp_scale_x[3],   cmp_scale_y[3];
    BYTE cmp_width[3],     cmp_height[3];

    BYTE i;
    BYTE _i;
    BYTE x1,               y1;
    BYTE _x1,              _y1;
    BYTE                   __y1;
    BYTE x2,               y2;
    BYTE du_x,             du_y;
    BYTE du_pos;

#ifdef DEBUG
    file_handle = fopen("debug.jpg", "wb");
#else
    if (file != NULL)
    {
        file_handle = fopen(file, "wb");
        if (file_handle == NULL) ERROR("target file invalid");
    }
    else
    {
        file_handle = stdout;
    #ifdef SYS_WIN32
        _setmode(_fileno(file_handle), _O_BINARY);
    #endif
        fputs("HTTP/1.0 200 OK\n", file_handle);
        fputs("Server: PWNC\n", file_handle);
        fputs("Content-Type: image/jpeg\n", file_handle);
        fputs("\n", file_handle);
    };
    fflush(file_handle);
#endif

    //reset bit buffer
    bitstring.count = 0;
    bitstring.value = 0;

    //adjust quality
    for (i = 0; i < 64; i++)
    {
        std_lqtable[i] = (BYTE)(std_lqtable[i] * QUALITY_LUMINANCE   / 100);
        if (std_lqtable[i] < 1) std_lqtable[i] = 1;
        std_cqtable[i] = (BYTE)(std_cqtable[i] * QUALITY_CHROMINANCE / 100);
        if (std_cqtable[i] < 1) std_cqtable[i] = 1;
    };

    //create huffman table
    jpeg_encoder_create_htable(std_dc_lhtable, std_dc_lhcount, std_dc_lhvalue);
    jpeg_encoder_create_htable(std_dc_chtable, std_dc_chcount, std_dc_chvalue);
    jpeg_encoder_create_htable(std_ac_lhtable, std_ac_lhcount, std_ac_lhvalue);
    jpeg_encoder_create_htable(std_ac_chtable, std_ac_chcount, std_ac_chvalue);

    //component information
    cmp[0].dc        = 0;
    cmp[0].qtable    = std_lqtable;
    cmp[0].htable_dc = std_dc_lhtable;
    cmp[0].htable_ac = std_ac_lhtable;

    for (i = 1; i < cmp_count; i++)
    {
        cmp[i].dc        = 0;
        cmp[i].qtable    = std_cqtable;
        cmp[i].htable_dc = std_dc_chtable;
        cmp[i].htable_ac = std_ac_chtable;
    };

    //SOI marker
    PUT_BYTE(0xFF); PUT_BYTE(SOI);
//.....................................................................................................
    //APP0 marker
    PUT_BYTE(0xFF); PUT_BYTE(APP0);
    //length
    PUT_WORD(16);
    //JFIF\0
    PUT_BYTE(0x4A); PUT_BYTE(0x46); PUT_BYTE(0x49); PUT_BYTE(0x46); PUT_BYTE(0x00);
    //version 1.0
    PUT_BYTE(0x01); PUT_BYTE(0x00);
    //aspect ratio (pixel)
    PUT_BYTE(0x00);
    //horizontal/vertical pixel density
    PUT_WORD(0x0001); PUT_WORD(0x0001);
    //thumbnail width/height
    PUT_BYTE(0x00); PUT_BYTE(0x00);
//.....................................................................................................
    //COM marker
    PUT_BYTE(0xFF); PUT_BYTE(COM);
    //length
    PUT_WORD(52);
    //comment
    for (i = 0; i < 50; i++) PUT_BYTE(comment[i]);
//.....................................................................................................
    //DHT marker
    PUT_BYTE(0xFF); PUT_BYTE(DHT);
    //length
    PUT_WORD((cmp_count > 1) ? 418 : 210);
    //dc luminance huffman table
    PUT_BYTE(0x00); //id
    for (i = 0; i <  16; i++) PUT_BYTE(std_dc_lhcount[i]);
    for (i = 0; i <  12; i++) PUT_BYTE(std_dc_lhvalue[i]);
    //ac luminance huffman table
    PUT_BYTE(0x10); //id
    for (i = 0; i <  16; i++) PUT_BYTE(std_ac_lhcount[i]);
    for (i = 0; i < 162; i++) PUT_BYTE(std_ac_lhvalue[i]);
    if (cmp_count > 1)
    {
        //dc chrominance huffman table
        PUT_BYTE(0x01); //id
        for (i = 0; i <  16; i++) PUT_BYTE(std_dc_chcount[i]);
        for (i = 0; i <  12; i++) PUT_BYTE(std_dc_chvalue[i]);
        //ac chrominance huffman table
        PUT_BYTE(0x11); //id
        for (i = 0; i <  16; i++) PUT_BYTE(std_ac_chcount[i]);
        for (i = 0; i < 162; i++) PUT_BYTE(std_ac_chvalue[i]);
    };
//.....................................................................................................
    //DQT marker
    PUT_BYTE(0xFF); PUT_BYTE(DQT);
    //length
    PUT_WORD((cmp_count > 1) ? 132 : 67);
    //luminance quantization table
    PUT_BYTE(0x00); //id
    for (i = 0; i < 64; i++) PUT_BYTE(std_lqtable[i]);
    if (cmp_count > 1)
    {
        //chrominance quantization table
        PUT_BYTE(0x01); //id
        for (i = 0; i < 64; i++) PUT_BYTE(std_cqtable[i]);
    };
//.....................................................................................................
    //SOF0 marker
    PUT_BYTE(0xFF); PUT_BYTE(SOF0);
    //length
    PUT_WORD((cmp_count > 1) ? 17 : 11);
    //precision
    PUT_BYTE(0x08);
    //height/width
    PUT_WORD(target.height); PUT_WORD(target.width);
    //component count
    PUT_BYTE(cmp_count);
    if (cmp_count > 1)
    {
        mcu_width        = 16; mcu_height       = 16;
        cmp_max_factor_x =  2; cmp_max_factor_y =  2;
        cmp_scale_x[0]   =  1; cmp_scale_y[0]   =  1;
        cmp_scale_x[1]   =  2; cmp_scale_y[1]   =  2;
        cmp_scale_x[2]   =  2; cmp_scale_y[2]   =  2;
        cmp_width[0]     = 16; cmp_height[0]    = 16;
        cmp_width[1]     =  8; cmp_height[1]    =  8;
        cmp_width[2]     =  8; cmp_height[2]    =  8;

        //Y component: id/factor/qtable id
        PUT_BYTE(0x01); PUT_BYTE(0x22); PUT_BYTE(0x00);
        //U component: id/factor/qtable id
        PUT_BYTE(0x02); PUT_BYTE(0x11); PUT_BYTE(0x01);
        //V component: id/factor/qtable id
        PUT_BYTE(0x03); PUT_BYTE(0x11); PUT_BYTE(0x01);
    }
    else
    {
        mcu_width        =  8; mcu_height       =  8;
        cmp_max_factor_x =  1; cmp_max_factor_y =  1;
        cmp_scale_x[0]   =  1; cmp_scale_y[0]   =  1;
        cmp_width[0]     =  8; cmp_height[0]    =  8;

        //Y component: id/factor/qtable id
        PUT_BYTE(0x01); PUT_BYTE(0x11); PUT_BYTE(0x00);
    };
//.....................................................................................................
    //SOS marker
    PUT_BYTE(0xFF); PUT_BYTE(SOS);
    //length
    PUT_WORD((cmp_count > 1) ? 12: 8);
    //component count
    PUT_BYTE(cmp_count);
    //Y component: id/htable id
    PUT_BYTE(0x01); PUT_BYTE(0x00);
    if (cmp_count > 1)
    {
        //U component: id/htable id
        PUT_BYTE(0x02); PUT_BYTE(0x11);
        //V component: id/htable id
        PUT_BYTE(0x03); PUT_BYTE(0x11);
    };
    //spectral selection start/end
    PUT_BYTE(0x00); PUT_BYTE(0x3F);
    //successive approximation high/low bit
    PUT_BYTE(0x00);

    //process scan data
    for (mcu_y = 0; mcu_y < target.height; mcu_y += mcu_height) //mcu vertical
    {
        for (mcu_x = 0; mcu_x < target.width; mcu_x += mcu_width) //mcu horizontal
        {
            for (i = 0; i < cmp_count; i++) //component
            {
                for (du_y = 0; du_y < cmp_height[i]; du_y += 8) //data unit vertical
                {
                    img_offset_y = mcu_y + du_y;

                    for (du_x = 0; du_x < cmp_width[i]; du_x += 8) //data unit horizontal
                    {
                        img_offset_x = mcu_x + du_x;

                        for (y1 = 0, _y1 = 0, __y1 = 0; y1 < 8; y1++, __y1 += 8)
                        {
                            img_y = img_offset_y + _y1;

                            for (x1 = 0, _x1 = 0; x1 < 8; x1++)
                            {
                                img_x              = img_offset_x + _x1;

                                du_pos             = x1 + __y1;
                                data_unit[du_pos]  = 0;
                                _i                 = 0;

                                //subsampling
                                for (y2 = 0; y2 < cmp_scale_y[i]; y2++)
                                {
                                    _img_y = img_y + y2;
                                    if (_img_y >= target.height) _img_y = target.height - 1;

                                    for (x2 = 0; x2 < cmp_scale_x[i]; x2++)
                                    {
                                        _img_x = img_x + x2;
                                        if (_img_x >= target.width) _img_x = target.width - 1;

                                        data_unit[du_pos] += target.cmp[i][_img_x + _img_y * target.width];
                                        _i++;
                                    };
                                };

                                data_unit[du_pos] /= _i;
                                _x1               += cmp_scale_x[i];
                            };

                            _y1 += cmp_scale_y[i];
                        };

                        //encoding
                        jpeg_encoder_fdct();
                        jpeg_encoder_quantize(i);
                        jpeg_encoder_put_data_unit(i);
                    }; //data unit horizontal
                }; //data unit vertical
            }; //component
        }; //mcu horizontal
    }; //mcu vertical

    if (bitstring.count) jpeg_encoder_put_bit(7, 0x7F);

    PUT_BYTE(0xFF); PUT_BYTE(EOI);

#ifdef DEBUG
    fclose(file_handle);
#else
    if (file != NULL) fclose(file_handle);
    else              fflush(file_handle);
#endif

    return 1;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_encoder_create_htable(STD_HTABLE *htable, const BYTE *hcount, const BYTE *hvalue)
{
    WORD code     = 0;
    BYTE position = 0;
    BYTE count;
    BYTE i;

    for (count = 0; count < 16; count++)
    {
        for (i = 0; i < hcount[count]; i++)
        {
            htable[hvalue[position]].value = code;
            htable[hvalue[position]].count = count + 1;
            position++;
            code++;
        };

        code <<= 1;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_encoder_put_bit(BYTE count, WORD value)
{
    DWORD buffer;

    buffer            = value & bitmask1[count];
    bitstring.count  += count;
    buffer          <<= 24 - bitstring.count;
    bitstring.value  |= buffer;

    while (bitstring.count >= 8)
    {
        buffer            = (bitstring.value >> 16) & 0xFF;

        PUT_BYTE(buffer);
        if (buffer == 0xFF) PUT_BYTE(0x00);

        bitstring.value <<= 8;
        bitstring.count  -= 8;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_encoder_put_data_unit(BYTE index)
{
    STD_HTABLE *htable;
    SWORD dc;
    WORD buffer;
    BYTE count;
    BYTE zrl;
    BYTE i;

    //DC coefficient
    htable        = cmp[index].htable_dc;
    dc            = data_unit[0] - cmp[index].dc;
    cmp[index].dc = data_unit[0];

    //bit count
    count         = 0;
    buffer        = (dc < 0) ? -dc : dc;
    while (buffer)
    {
        count++;
        buffer >>= 1;
    };

    //emit bit count
    jpeg_encoder_put_bit(htable[count].count, htable[count].value);

    //emit bit value
    jpeg_encoder_put_bit(count, CONVERT_WORD(dc, count));

    //AC coefficient
    htable      = cmp[index].htable_ac;
    zrl         = 0;

    for (i = 1; i < 64; i++)
    {
        if (! data_unit[i])
        {
            zrl++;
        }
        else
        {
            while (zrl >= 16)
            {
                jpeg_encoder_put_bit(htable[0xF0].count, htable[0xF0].value);
                zrl -= 16;
            };

            //bit count
            buffer = (data_unit[i] < 0) ? -data_unit[i] : data_unit[i];
            count  = 1;
            while (buffer >>= 1) count++;

            //emit zrl/bit count huffman code
            buffer = BYTE_HIGH_LOW(zrl, count);
            jpeg_encoder_put_bit(htable[buffer].count, htable[buffer].value);

            //emit bit value
            jpeg_encoder_put_bit(count, CONVERT_WORD(data_unit[i], count));

            zrl    = 0;
        };
    };

    if (zrl) jpeg_encoder_put_bit(htable[0x00].count, htable[0x00].value); //EOB
};
//-----------------------------------------------------------------------------------------------------
void jpeg_encoder_fdct(void)
{
    SDWORD a0, a1, a2, a3, a4, a5, a6, a7, a8;
    SWORD *p;
    BYTE i;

    for (i = 0; i < 64; i++) data_unit[i] -= 128;

    //process row
    p = data_unit;

    for (i = 0; i < 8; i++)
    {
        a8     = p[0] + p[7];
        a0     = p[0] - p[7];
        a7     = p[1] + p[6];
        a1     = p[1] - p[6];
        a6     = p[2] + p[5];
        a2     = p[2] - p[5];
        a5     = p[3] + p[4];
        a3     = p[3] - p[4];
        a4     = a8 + a5;
        a8    -= a5;
        a5     = a7 + a6;
        a7    -= a6;

        p[0]   = a4 + a5;
        p[4]   = a4 - a5;

        p[2]   = (a8 * 1338 + a7 *  554) >> 10;
        p[6]   = (a8 *  554 - a7 * 1338) >> 10;

        p[7]   = (a0 *  283 - a1 *  805 + a2 * 1204 - a3 * 1420) >> 10;
        p[5]   = (a0 *  805 - a1 * 1420 + a2 *  283 + a3 * 1204) >> 10;
        p[3]   = (a0 * 1204 - a1 *  283 - a2 * 1420 - a3 *  805) >> 10;
        p[1]   = (a0 * 1420 + a1 * 1204 + a2 *  805 + a3 *  283) >> 10;

        p     += 8;
    };

    //process column
    p = data_unit;

    for (i = 0; i < 8; i++)
    {
        a8     = p[ 0] + p[56];
        a0     = p[ 0] - p[56];
        a7     = p[ 8] + p[48];
        a1     = p[ 8] - p[48];
        a6     = p[16] + p[40];
        a2     = p[16] - p[40];
        a5     = p[24] + p[32];
        a3     = p[24] - p[32];
        a4     = a8 + a5;
        a8    -= a5;
        a5     = a7 + a6;
        a7    -= a6;

        p[ 0]  = (a4 + a5) >> 3;
        p[32]  = (a4 - a5) >> 3;

        p[16]  = (a8 * 1338 + a7 *  554) >> 13;
        p[48]  = (a8 *  554 - a7 * 1338) >> 13;

        p[56]  = (a0 *  283 - a1 *  805 + a2 * 1204 - a3 * 1420) >> 13;
        p[40]  = (a0 *  805 - a1 * 1420 + a2 *  283 + a3 * 1204) >> 13;
        p[24]  = (a0 * 1204 - a1 *  283 - a2 * 1420 - a3 *  805) >> 13;
        p[ 8]  = (a0 * 1420 + a1 * 1204 + a2 *  805 + a3 *  283) >> 13;

        p     += 1;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_encoder_quantize(BYTE index)
{
    BYTE i;
    SWORD _data_unit[64];
    BYTE threshold = (index == 0) ? THRESHOLD_LUMINANCE : THRESHOLD_CHROMINANCE;

    for (i = 0; i < 64; i++)
        _data_unit[i]        = (data_unit[i] >= -threshold && data_unit[i] <= threshold) ?
                               0 : data_unit[i] / cmp[index].qtable[stable[i]];
    for (i = 0; i < 64; i++)
        data_unit[stable[i]] =  _data_unit[i];
};
