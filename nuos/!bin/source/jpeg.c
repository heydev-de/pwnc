//-----------------------------------------------------------------------------------------------------
void jpeg_pdecoder_dc_base(BYTE index, SWORD *buffer)
{
    SWORD value;
    BYTE count;

    if (count = jpeg_decoder_get_hvalue(cmp[index].htable_dc))
    {
        value         = jpeg_decoder_get_bit(count);
        value         = CONVERT_SWORD(value, count);
        cmp[index].dc = value += cmp[index].dc;
        buffer[0]     = value << scan.sa_low;
    }
    else
    {
        buffer[0]     = cmp[index].dc << scan.sa_low;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_pdecoder_dc_approximation(BYTE index, SWORD *buffer)
{
    if (jpeg_decoder_get_bit(1)) buffer[0] += 1 << scan.sa_low;
};
//-----------------------------------------------------------------------------------------------------
void jpeg_pdecoder_ac_base(BYTE index, SWORD *buffer)
{
    SDWORD value;
    BYTE zero = 0;
    BYTE count;
    BYTE i;

    if (scan.eobrun)
    {
        scan.eobrun--;
        return;
    };

    for (i = scan.ss_start; i <= scan.ss_end; i++)
    {
        value  = jpeg_decoder_get_hvalue(cmp[index].htable_ac);
        zero   = BYTE_HIGH(value);
        count  = BYTE_LOW(value);

        if (count)
        {
            i                          += zero;
            value                       = jpeg_decoder_get_bit(count);
            value                       = CONVERT_SWORD(value, count);
            buffer[buffer_position[i]]  = value << scan.sa_low;
        }
        else if (zero == 15)
        {
            i                          += 15;
        }
        else
        {
            scan.eobrun                 = 1 << zero;
            if (zero) scan.eobrun += jpeg_decoder_get_bit(zero);
            scan.eobrun--;
            break;
        };
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_pdecoder_ac_approximation(BYTE index, SWORD *buffer)
{
    SWORD value;
    SWORD positive =  1 << scan.sa_low;
    SWORD negative = -1 << scan.sa_low;
    SWORD *coefficient;
    BYTE zero;
    BYTE count;
    BYTE i         = scan.ss_start;

    if (! scan.eobrun)
    {
        for (; i <= scan.ss_end; i++)
        {
            value = jpeg_decoder_get_hvalue(cmp[index].htable_ac);
            zero  = BYTE_HIGH(value);
            count = BYTE_LOW(value);

            if (count)
            {
                value       = jpeg_decoder_get_bit(1) ? positive : negative;
            }
            else if (zero != 15)
            {
                scan.eobrun = 1 << zero;
                if (zero) scan.eobrun += jpeg_decoder_get_bit(zero);
                break;
            };

            do
            {
                coefficient = &buffer[buffer_position[i]];

                if (*coefficient)
                {
                    if (jpeg_decoder_get_bit(1))
                    {
                        if (! (*coefficient & positive))
                        {
                            *coefficient += (*coefficient > 0) ? positive : negative;
                        };
                    };
                }
                else
                {
                    if (! zero) break;
                    zero--;
                };

                i++;
            }
            while (i <= scan.ss_end);

            if (count) buffer[buffer_position[i]] = value;
        };
    };

    if (scan.eobrun)
    {
        for (; i <= scan.ss_end; i++)
        {
            coefficient = &buffer[buffer_position[i]];

            if (*coefficient)
            {
                if (jpeg_decoder_get_bit(1))
                {
                    if (! (*coefficient & positive))
                    {
                        *coefficient += (*coefficient > 0) ? positive : negative;
                    };
                };
            };
        };

        scan.eobrun--;
    };
};
//-----------------------------------------------------------------------------------------------------
void jpeg_pdecoder_dequantize(BYTE index, SWORD *buffer)
{
    BYTE i;

    for (i = 0; i < 64; i++) data_unit[i] = buffer[buffer_position[stable[i]]] * cmp[index].qtable[stable[i]];
};

//-----------------------------------------------------------------------------------------------------
//===< RESAMPLER >=====================================================================================
//-----------------------------------------------------------------------------------------------------

int jpeg_resample(void)
{
    //downscale
    WORD img_width_half;
    WORD img_height_half;
    DWORD buffer_width_double;

    //scale
    double factor_x,     factor_y;
    double fraction_x,   fraction_y;
    double _fraction_x,  _fraction_y;
    double __fraction_x, __fraction_y;

    //buffer image
    DWORD source_x1,     source_y1;
    DWORD source_x2,     source_y2;
    DWORD source_x3,     source_y3;
    DWORD source_x4,     source_y4;

    //buffer line
    BYTE line1;
    BYTE line2;
    BYTE line3;
    BYTE line4;
    BYTE value;

    //target image
    WORD target_x,       target_y;
    DWORD _target,       _target_y;

    BYTE i;

    //downscale
    img_width_half      = img_width  / 2;
    img_height_half     = img_height / 2;
    buffer_width_double = buffer.width + buffer.width;

    while (target.width < img_width_half && target.height < img_height_half)
    {
        source_y1 = 0;
        source_y2 = buffer.width;

        for (target_y = 0; target_y < img_height_half; target_y++)
        {
            source_x1 = 0;
            source_x2 = 1;
            _target_y = target_y * buffer.width;

            for (target_x = 0; target_x < img_width_half; target_x++)
            {
                _target = target_x + _target_y;

                for (i = 0; i < cmp_count; i++)
                {
                    if (i == 0)
                    {
                        value = ((BYTE)buffer.cmp[i][source_x1 + source_y1] +
                                 (BYTE)buffer.cmp[i][source_x1 + source_y2] +
                                 (BYTE)buffer.cmp[i][source_x2 + source_y1] +
                                 (BYTE)buffer.cmp[i][source_x2 + source_y2]) / 4;
                    }
                    else
                    {
                        value = (BYTE)buffer.cmp[i][source_x1 + source_y1];
                    };

                    buffer.cmp[i][_target] = value;
                };

                source_x1 += 2;
                source_x2 += 2;
            };

            source_y1 += buffer_width_double;
            source_y2 += buffer_width_double;
        };

        img_width       = img_width_half;
        img_height      = img_height_half;
        img_width_half  = img_width  / 2;
        img_height_half = img_height / 2;
    };

    //target image size
    if (target.width == 0)       target.width  = target.height * img_width  / img_height;
    else if (target.height == 0) target.height = target.width  * img_height / img_width;

    //target buffer allocation
    target.size = target.width * target.height * sizeof(BYTE);

    for (i = 0; i < cmp_count; i++)
    {
        target.cmp[i] = (BYTE *)malloc(target.size);
        if (target.cmp[i] == NULL) ERROR("out of memory");
    };

    factor_x = (double)img_width  / (double)target.width;
    factor_y = (double)img_height / (double)target.height;

    //bicubic interpolation
    for (target_y = 0; target_y < target.height; target_y++)
    {
        fraction_y    = target_y * factor_y;

        source_y2     = (WORD)fraction_y;
        source_y1     = (source_y2     >  0)          ? source_y2  - 1 : 0;
        source_y3     = (source_y2 + 1 >= img_height) ? img_height - 1 : source_y2 + 1;
        source_y4     = (source_y2 + 2 >= img_height) ? img_height - 1 : source_y2 + 2;

        _fraction_y   = fraction_y - source_y2;
        __fraction_y  = 1.0 - _fraction_y;

        source_y1    *= buffer.width;
        source_y2    *= buffer.width;
        source_y3    *= buffer.width;
        source_y4    *= buffer.width;
        _target_y     = target_y * target.width;

        for (target_x = 0; target_x < target.width; target_x++)
        {
            fraction_x   = target_x * factor_x;

            source_x2    = (WORD)fraction_x;
            source_x1    = (source_x2     >  0)         ? source_x2 - 1 : 0;
            source_x3    = (source_x2 + 1 >= img_width) ? img_width - 1 : source_x2 + 1;
            source_x4    = (source_x2 + 2 >= img_width) ? img_width - 1 : source_x2 + 2;
            _target      = target_x + _target_y;

            _fraction_x  = fraction_x - source_x2;
            __fraction_x = 1.0 - _fraction_x;

            for (i = 0; i < cmp_count; i++)
            {
                if (i == 0) //bicubic (luminance)
                {
                    line1 = jpeg_resampler_cubic((BYTE)buffer.cmp[i][source_x1 + source_y1],
                                                 (BYTE)buffer.cmp[i][source_x2 + source_y1],
                                                 (BYTE)buffer.cmp[i][source_x3 + source_y1],
                                                 (BYTE)buffer.cmp[i][source_x4 + source_y1],
                                                 _fraction_x);
                    line2 = jpeg_resampler_cubic((BYTE)buffer.cmp[i][source_x1 + source_y2],
                                                 (BYTE)buffer.cmp[i][source_x2 + source_y2],
                                                 (BYTE)buffer.cmp[i][source_x3 + source_y2],
                                                 (BYTE)buffer.cmp[i][source_x4 + source_y2],
                                                 _fraction_x);
                    line3 = jpeg_resampler_cubic((BYTE)buffer.cmp[i][source_x1 + source_y3],
                                                 (BYTE)buffer.cmp[i][source_x2 + source_y3],
                                                 (BYTE)buffer.cmp[i][source_x3 + source_y3],
                                                 (BYTE)buffer.cmp[i][source_x4 + source_y3],
                                                 _fraction_x);
                    line4 = jpeg_resampler_cubic((BYTE)buffer.cmp[i][source_x1 + source_y4],
                                                 (BYTE)buffer.cmp[i][source_x2 + source_y4],
                                                 (BYTE)buffer.cmp[i][source_x3 + source_y4],
                                                 (BYTE)buffer.cmp[i][source_x4 + source_y4],
                                                 _fraction_x);
                    value = jpeg_resampler_cubic(line1, line2, line3, line4, _fraction_y);
                }
                else //bilinear (chrominance)
                {
                    line1 = (BYTE)(buffer.cmp[i][source_x1 + source_y1] * __fraction_x +
                                   buffer.cmp[i][source_x2 + source_y1] * _fraction_x);
                    line2 = (BYTE)(buffer.cmp[i][source_x1 + source_y2] * __fraction_x +
                                   buffer.cmp[i][source_x2 + source_y2] * _fraction_x);
                    value = (BYTE)(line1 * __fraction_y + line2 * _fraction_y);
                };

                target.cmp[i][_target] = value;
            };
        };
    };

    return 1;
};
//-----------------------------------------------------------------------------------------------------
BYTE jpeg_resampler_cubic(BYTE value1, BYTE value2, BYTE value3, BYTE value4, double fraction)
{
    double fraction_sqare = fraction * fraction;

    SWORD a = value1 - value2;
    SWORD b = value4 - value3 - a;
    SWORD c = a - b;
    SWORD d = value3 - value1;

    SWORD e = (SWORD)(b * fraction_sqare * fraction);
    SWORD f = (SWORD)(c * fraction_sqare);
    SWORD g = (SWORD)(d * fraction);

    SWORD i = e + f + g + value2;

    if (i < 0)        return   0;
    else if (i > 255) return 255;
    return (BYTE)i;
};

